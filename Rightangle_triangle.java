import java.util.Scanner;
class Rightangle_triangle 
{
public static void main (str[]args)
{
scanner input = new scanner(system.in);
int n=5;
for(i=1;i<=5;i++)
{
for(j=0;j<=n-i;j++)
{
system.out.print("  ");
}
for(k=1;k<=n;k++)
{
system.out.print(" * ");
}
system.out.println();
}
}
}

